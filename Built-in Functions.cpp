//WAP to learn about Built-in Functions

#include<iostream>
#include<cmath>
using namespace std;

int main(){
	cout<<"abs(3.5):      "<<abs(3.5)<<endl;
	cout<<"abs(-3.5):     "<<abs(-3.5)<<endl;
	cout<<"ceil(59.76):   "<<ceil(59.76)<<endl;
	cout<<"ceil(-59.76):  "<<ceil(-59.76)<<endl;
	cout<<"exp(1):        "<<exp(1)<<endl;
	cout<<"fabs(3.5):     "<<fabs(3.5)<<endl;
	cout<<"cos(0):        "<<cos(0)<<endl;
	cout<<"floor(40.8):   "<<floor(48.8)<<endl;
	cout<<"floor(-40.8):  "<<floor(-48.8)<<endl;
	cout<<"tolower(65):   "<<tolower(65)<<endl;
	cout<<"toupper(97):   "<<toupper(97)<<endl;
	cout<<"sqrt(4):       "<<sqrt(4)<<endl;
	cout<<"sqrt(-4):      "<<sqrt(-4)<<endl;
	cout<<"log(5):        "<<log(5)<<endl;
	cout<<"log10(10):     "<<log10(10)<<endl;
	cout<<"sin(0):        "<<sin(0)<<endl;
	cout<<"tan(0):        "<<tan(0)<<endl;
	cout<<"fmod(10,3):    "<<fmod(10,3)<<endl;
	cout<<"fmod(10.5,3):  "<<fmod(10.5,3)<<endl;
	return 0;
}
